uint16_t ISRCNT = 0;
uint32_t lastHT = 0;

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin) {
	if (GPIO_Pin == EXI1_Pin) {
		if(GPIO_PIN_SET == HAL_GPIO_ReadPin(EXI1_GPIO_Port, EXI1_Pin) )
		{
			lastHT = HAL_GetTick();
		}else
		{
			if( HAL_GetTick() - lastHT >=100){
				ISRCNT++;
			}
		}

	}
}
